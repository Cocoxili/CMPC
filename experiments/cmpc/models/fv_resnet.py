class ChannelPooling(nn.AdaptiveAvgPool1d):
    def forward(self, input):
        input = torch.unsqueeze(input, dim=1)
        pooled = nn.AdaptiveAvgPool1d(self.output_size)
        output = pooled(input)
        output = torch.squeeze(output, dim=1)
        return output


